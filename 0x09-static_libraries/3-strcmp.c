#include "main.h"
#include <stdio.h>
#include <string.h>
/**
 * _strcmp - function to compare strings based on their lexicographical order
 * @s1: input parameter
 * @s2: input parameter
 * Return: difference between the ascii values of the string
 */
int _strcmp(char *s1, char *s2)
{
int res = strcmp(s1, s2);
return (res);
}
